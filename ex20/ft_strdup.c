#include <stdlib.h>

char		*ft_strdup(char *src)
{
	int		i;
	char	*r;

	i = 0;
	while (src[i] != '\0')
		i++;
	r = malloc(i * sizeof(char));
	r[i] = '\0';
	while (i-- >= 0)
		r[i] = src[i];
	return (r);
}

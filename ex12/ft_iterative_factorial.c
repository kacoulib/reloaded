int		ft_iterative_factorial(int nb)
{
	int i;
	int r;

	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	if (nb == 0)
	{
		return (1);
	}
	i = 0;
	r = 1;
	while (i++ < nb)
	{
		r *= i;
	}
	return (r);
}
